package edu.gonzaga;

public class Player {

    protected String name;
    protected int score;

    public Player(String name) {
        this.name = name;
        this.score = score;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public int getScore(int score) {
        return score;
    }

    public void setScore(int score){
        this.score = score;
    }

}